package com.belk.car.app.webapp.forms;

/**
 * This class contains form fields mapped with vendor catalog notes form
 * @author afusya2
 */
public class VendorCatalogNotesForm {

	private String note;
	private String noteSubject;
	/**
	 * @return the note
	 */
	public String getNote() {
		return note;
	}
	/**
	 * @param note the note to set
	 */
	public void setNote(String note) {
		this.note = note;
	}
	/**
	 * @return the noteSubject
	 */
	public String getNoteSubject() {
		return noteSubject;
	}
	/**
	 * @param noteSubject the noteSubject to set
	 */
	public void setNoteSubject(String noteSubject) {
		this.noteSubject = noteSubject;
	}
}
